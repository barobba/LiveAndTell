if (!$query_string) {
  $flash_url = url("v/$node->nid", array('absolute' => true));
} else {
  $flash_url = url("v/$node->nid", array('query' => $query_string, 'absolute' => true));
}

$flash_vars = 'fontFamily=DejaVu Sans,Aboriginal Serif,Aboriginal Serif Regular,Aboriginal Sans,Aboriginal Sans Regular';

print <<<EOS
<object width="$width" height="$height">
  <param name="FlashVars" value="$flash_vars"></param>
  <param name="movie" value="$flash_url"></param>
  <param name="allowFullScreen" value="true"></param>
  <param name="wmode" value="transparent"></param>
  <embed
    FlashVars       = "$flash_vars"
    src             = "$flash_url" 
    wmode           = "transparent"
    type            = "application/x-shockwave-flash" 
    allowfullscreen = "true"
    width           = "$width"
    height          = "$height"
  ></embed>
</object>
EOS;
